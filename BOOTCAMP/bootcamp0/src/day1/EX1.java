package day1;

import java.util.Scanner;

public class EX1 {
	public static void main(String[] args) {
		int num1;
		Scanner sc = new Scanner(System.in);
		System.out.print("정수를 입력하세요 ===>");
		num1 = sc.nextInt();
		System.out.printf("%d\n",num1);
		System.out.printf("%x\n",num1);
		System.out.printf("%o\n",num1);
		
	}

}
